// 2. **理解混淆电路协议**
//    - 阅读 Yao 1982 原始论文
//    - 理解布尔电路表示
//    - 了解 OT 协议的作用
//
// 3. **测试和调试**
//    - 先使用简单的电路（如 AND 门）测试
//    - 逐步增加复杂度
//    - 使用 InMemoryChannel 进行本地测试
//
// 4. **性能优化**
//    - 启用 LTO (lto = true)
//    - 使用 release 模式编译
//    - 考虑电路大小和网络延迟

