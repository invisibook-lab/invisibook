// ============================================================================
// 2. 基本使用流程
// ============================================================================
//
// swanky 的两方安全计算（混淆电路）基本流程：
//
// 1. 建立通信通道（Channel）
// 2. 初始化不经意传输（OT）协议
// 3. 创建混淆电路生成器（Garbler）和评估器（Evaluator）
// 4. 定义要计算的函数（转换为布尔电路）
// 5. 执行混淆电路协议：
//    - Garbler 生成混淆表
//    - Evaluator 通过 OT 获取输入标签
//    - Evaluator 评估电路
//    - 双方协作解码输出

